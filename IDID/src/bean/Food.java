package bean;

public class Food {
	private String f_kind;
	private String f_name;
	private int f_cal;
	public String getF_kind() {
		return f_kind;
	}
	public void setF_kind(String f_kind) {
		this.f_kind = f_kind;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public int getF_cal() {
		return f_cal;
	}
	public void setF_cal(int f_cal) {
		this.f_cal = f_cal;
	}
}
